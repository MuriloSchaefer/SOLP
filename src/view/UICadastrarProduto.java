    private void addMateriaPrimaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addMateriaPrimaActionPerformed
        // TODO add your handling code here:
        /**
        * Quando o usuario clicar no botão para adicionar um novo endereço,
        * será pego o modelo atual da tabela (linhas ja existentes) e então
        * adicionada uma nova linha correspondente aos valores preechidos.
        */

        DefaultTableModel dtm = (DefaultTableModel)tblMateriaPrima.getModel();
        
        double quantidade = (double) txtQuantidade.getValue();
        MateriaPrima materiaPrima = listaMateriaPrima.get(comboMateriaPrima.getSelectedIndex());
        double valorAcumulado =  quantidade * materiaPrima.getValorUnd();
        total += valorAcumulado;
        Object[] linha = {materiaPrima.getNome(),
                          txtQuantidade.getValue(),
                          materiaPrima.getValorUnd(),
                          valorAcumulado
        };
        dtm.addRow(linha);
        tblMateriaPrima.setModel(dtm);
        String strTotal = String.valueOf(total);
        txtTotal.setText(strTotal);
    }//GEN-LAST:event_addMateriaPrimaActionPerformed

    private void removeMateriaPrimaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeMateriaPrimaActionPerformed
        // TODO add your handling code here:
        /**
        * Quando o usuario clicar no botão excluir endereco, será pego a linha
        * selecionada na tabela e então está será removida, caso não tenha uma linha
        * selecionada, nada será feito.
        */
        int linha = tblMateriaPrima.getSelectedRow(); //pega a linha selecionada
        if(linha >= 0){ //se realmente houver uma linha selecionada, então
            DefaultTableModel dtm = (DefaultTableModel)tblMateriaPrima.getModel();
            String strValor = dtm.getValueAt(linha, 3).toString();
            double valor = Double.valueOf(strValor);
            total -= valor;
            strValor = String.valueOf(total);
            txtTotal.setText(strValor);
            dtm.removeRow(linha);
            tblMateriaPrima.setModel(dtm);
        }
    }//GEN-LAST:event_removeMateriaPrimaActionPerformed

