    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        int qntd = tblMateriaPrima.getRowCount(); // pega a quantidade de linhas da tabela
        DefaultTableModel dtm = (DefaultTableModel)tblMateriaPrima.getModel(); //responsavel pela leitura da tabela
        List<MateriaPrimaPorProduto> materiasPrima = new ArrayList<>(); //cria uma lista de materiasPrimasPorProduto (ver o diagrama de classe)
        Produto produto = new Produto(); //cria um novo produto
        boolean add = false; //validará a inserção
        for(int i=0; i<qntd; i++){
            MateriaPrimaPorProduto materiaPrima = new MateriaPrimaPorProduto(); //cria uma nova MateriaPrimaPorProduto
            
            try {
                listaMateriaPrima = controladorMateriaPrima.consultar(dtm.getValueAt(i, 0).toString()); //realiza uma busca na materia prima que esta na linha i
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(UICadastrarProduto.class.getName()).log(Level.SEVERE, null, ex);
            }
            materiaPrima.setMateriaPrima(listaMateriaPrima.get(0)); //atribui a materia prima para a materiaPrimaPorProduto, caso a busca tenha retornado mais de um valor, sempre pegará o primeiro
            String valor = dtm.getValueAt(i, 1).toString(); //pega o campo quantidade
            double valorD = Double.valueOf(valor); //transforma a quantidade em double
            materiaPrima.setQuantidade(valorD); //atribui a quantidade no objeto materiaPrima
            valor = dtm.getValueAt(i, 3).toString(); //pega o campo valorAcumulado
            valorD = Double.valueOf(valor); //transforma para double
            materiaPrima.setTotalAcumulado(valorD); //atribui o valorAcumulado no objeto materiaPrima
            materiasPrima.add(materiaPrima); //adiciona a MateriaPrimaPorProduto na lista
        }
        produto.setNome(txtNome.getText()); //atribui valores para o produto
        produto.setDescricao(txtDescricao.getText());
        produto.setMateriasPrimas(materiasPrima);
        produto.setTotal(total);
        try {
            add = controladorProduto.add(produto); //envia para o controlador fazer a inserção do produto
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(UICadastrarProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(add){ //caso consiga fazer a inserção
            JOptionPane.showMessageDialog(rootPane, "Produto cadastrado com sucesso!");
            this.dispose();
        } else{ //caso nao consiga
            JOptionPane.showMessageDialog(rootPane, "Produto não cadastrado!");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

