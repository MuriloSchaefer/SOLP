    private void addEnderecoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addEnderecoActionPerformed
        // TODO add your handling code here:
        if(!CadEnd){
            CadEnd = true;
            PanelEndereco.setVisible(true);
            addEndereco.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imgs/remove.png")));
            enderecoSel.setVisible(false);
        } else {
            CadEnd = false;
            PanelEndereco.setVisible(false);
            addEndereco.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imgs/add.png")));
            enderecoSel.setVisible(true);
        }
    }//GEN-LAST:event_addEnderecoActionPerformed

