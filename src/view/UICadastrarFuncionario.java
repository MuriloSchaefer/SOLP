    /**
    * Quando o usuário clicar em cadastrar será criado um objeto do tipo endereco
    * e um objeto do tipo funcionario. Após isto ambos os objetos serão enviados à
    * classe controladorFuncionario, que fará o cadastro do endereço e do funcionario
    * associando um ao outro 
    *
    */
    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        
        boolean add = false; //variavel para validar a inserção
        
        if(CadEnd){
            //Cria objeto do tipo Endereco ------------------------------
            Endereco endereco = new Endereco();
            endereco.setRua(txtRua.getText());
            endereco.setBairro(txtBairro.getText());
            endereco.setCidade(txtCidade.getText());
            endereco.setEstado(txtUF.getSelectedItem().toString());
            endereco.setNum((int)txtNum.getValue());
            //-----------------------------------------------------------

            //Cria objeto do tipo Funcionario ---------------------------
            Funcionario funcionario = new Funcionario();
            funcionario.setNome(txtNome.getText());
            funcionario.setCpf(txtCpf.getText());
            funcionario.setRg(txtRg.getText());
            funcionario.setUsuario(txtUsuario.getText());
            funcionario.setSenha(txtSenha.getText());
            funcionario.setPermissoes(comboPermissoes.getSelectedIndex());
            //------------------------------------------------------------

            try {
                add = controladorFuncionario.add(endereco, funcionario); //envia para o controlador
            } catch (ClassNotFoundException | SQLException | NoSuchAlgorithmException ex) {
                Logger.getLogger(UICadastrarFuncionario.class.getName()).log(Level.SEVERE, null, ex); //caso de algum erro, vem pra ca
            }
        } else {
            //Cria objeto do tipo Funcionario ---------------------------
            Funcionario funcionario = new Funcionario();
            funcionario.setNome(txtNome.getText());
            funcionario.setCpf(txtCpf.getText());
            funcionario.setRg(txtRg.getText());
            funcionario.setUsuario(txtUsuario.getText());
            funcionario.setSenha(txtSenha.getText());
            funcionario.setPermissoes(comboPermissoes.getSelectedIndex());
            funcionario.setEndereco(lista.get(enderecoSel.getSelectedIndex()).getId());
            //------------------------------------------------------------

            try {
                add = controladorFuncionario.add(funcionario); //envia para o controlador
            } catch (ClassNotFoundException | SQLException | NoSuchAlgorithmException ex) {
                Logger.getLogger(UICadastrarFuncionario.class.getName()).log(Level.SEVERE, null, ex); //caso de algum erro, vem pra ca
            }
        }
        if(add){ //caso consiga inserir um novo funcionario
            JOptionPane.showMessageDialog(rootPane, "Funcionario cadastrado com sucesso!");
            this.dispose();
        } else{ //caso não consiga inserir um novo funcionario
            JOptionPane.showMessageDialog(rootPane, "Funcionario nao cadastrado!");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    
